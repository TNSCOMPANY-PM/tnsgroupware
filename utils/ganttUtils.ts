export function getHolidayName(date: Date): string | null {
  const str = format(date, "yyyy-MM-dd");
  return HOLIDAY_NAMES[str] ?? null;
}

export function isWeekend(date: Date): boolean {
  const d = date.getDay();
  return d === 0 || d === 6;
}

export function isHoliday(date: Date): boolean {
  const y = date.getFullYear();
  const list = HOLIDAYS[y];
  if (!list) return false;
  const str = format(date, "yyyy-MM-dd");
  return list.includes(str);
}

export function isWeekendOrHoliday(date: Date): boolean {
  return isWeekend(date) || isHoliday(date);
}

export function getDatesInRange(start: Date, end: Date): Date[] {
  const result: Date[] = [];
  let d = startOfDay(start);
  const endDay = startOfDay(end);
  while (d <= endDay) {
    result.push(d);
    d = addDays(d, 1);
  }
  return result;
}

export function dateToDayIndex(date: Date, chartStart: Date): number {
  return differenceInDays(startOfDay(date), startOfDay(chartStart));
}

export function dayIndexToDate(index: number, chartStart: Date): Date {
  return addDays(startOfDay(chartStart), index);
}

export function datesOverlap(
  aStart: Date,
  aEnd: Date,
  bStart: Date,
  bEnd: Date
): boolean {
  return !(isAfter(aStart, bEnd) || isBefore(aEnd, bStart));
}

export function parseDateSafe(str: string): Date {
  return startOfDay(parseISO(str));
}
